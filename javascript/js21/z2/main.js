function changeCard(card_1){
    let card = document.getElementById(card_1)
    if(card.classList.contains('yellow')){
        card.classList.remove('yellow')
    }else{
        card.classList.add('yellow')
    }
    if(card.classList.contains('bg-red')){
        card.classList.remove('bg-red')
    }else{
        card.classList.add('bg-red')
    }
}
